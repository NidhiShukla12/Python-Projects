
# Q.1 Write a program for arithmatic operators

# Arithmetic Operators : Arithmetic operators are symbols used in programming and mathematics to perform arithmetic operations on numerical values. These operators take one or more numbers as input and produce a numerical result.

# Here are the common arithmetic operators:

# 1. Addition (+): Adds two or more numbers.
# Example: a + b

num1 = 10
num2 = 5
add = num1 + num2
print(add) # it gives the addition of both the numbers. (output will be = 15)

# 2. Subtraction (-): Subtracts one number from another.
# Example: a - b

num1 = 10
num2 = 5
sub = num1 - num2
print(sub) # it gives the subtraction of both the numbers (output will be = 5)

# 3. Multiplication (*): Multiplies two or more numbers.
# Example: a * b

num1 = 10
num2 = 50
result = num1 * num2
print(result) # it gives the multiplication of both the numbers(output will be = 500)

# 4. Division (/): Divides one number by another.
# Example: a / b

num1 = 100
num2 = 50
result = num1 / num2
print(result) # it gives the complete division(output will be = 2.0)

#5. Modulus (%): Returns the remainder of a division operation.
# Example: a % b

num1 = 105
num2 = 50
result = num1 % num2
print(result) # it gives the remainder(output will be = 5)

#6. Exponentiation (**): Raises a number to a power.
# Example: a ** b

num1 = 10
num2 = 5
result = num1 ** num2
print(result) # it gives the multiplication of 10 , 5 times (output will be = 100000)

#7. Floor Division (//): Divides one number by another and returns the largest possible integer.
# Example: a // b

num1 = 105
num2 = 50
result = num1 // num2
print(result) # it gives the only integer value (output will be = 2)


